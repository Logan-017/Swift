// MARK:- 求余/取模 - 只与[被除数]符号有关
print("9 % 4 = \(9 % 4)")
print("-9 % 4 = \(-9 % 4)")
print("--------------------")

// MARK:- 求余/取模 - 与[除数]符号无关
print("9 % 4 = \(9 % 4)")
print("9 % -4 = \(9 % -4)")

